const express = require('express');
const app = express();
app.use(express.json());
const Registration = require('../controller/registrationController');
const activityLogsController = require('../controller/activityLoggerController');
const signupValidate = require('../utils/validation/signupValidation');
const signinValidate = require('../utils/validation/signinValidation');
const smsCode = require('../services/sms/message');
// const otp = require('../services/sms/otp');
const headers = require('../utils/tokenmanager/headers');
const token = require('../utils/tokenmanager/token');

app.post('/register/signup', headers, signupValidate, smsCode, Registration.signUp);
app.post('/register/login-password', headers, signinValidate, Registration.loginWithPassword);
app.post('/register/login-otp', headers, Registration.generate_otp);
app.post('/register/detail', token, headers, Registration.detail);
app.post('/register/list', token, headers, Registration.list);
app.post('/register/delete', token, headers, Registration.delete);
app.post('/register/deactivate', token, headers, Registration.deactivate);
app.post('/register/activate', token, headers, Registration.activate);
app.post('/register/update', token, headers, Registration.update);
app.post('/register/logout', token, headers, Registration.logout);
app.post('/register/forgotpassword', token, headers, Registration.forgotPassword);
app.post('/register/resetpassword', token, headers, Registration.resetPassword);
app.post('/register/password', token, headers, Registration.password);
app.post('/register/activityLogsList', token, headers, activityLogsController.activityLogsList);



